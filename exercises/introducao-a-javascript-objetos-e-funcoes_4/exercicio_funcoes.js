function sum (a, b) {
  return a + b;     
};
function sub (a, b) {
  return a - b
};
function mult (a, b) {
  return a * b
};
function divi (a, b) {
  return a / b
};
function modulo (a, b) {
  return a % b
}

console.log(sum(10, 2));
console.log(sub(10, 2));
console.log(mult(10, 2));
console.log(divi(10, 2));
console.log(modulo(10, 2));